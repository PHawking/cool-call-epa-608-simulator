"use strict";

const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "..");
const read = name => fs.readFileSync(path.join(root, name), "utf8").trimEnd();
let html = read("index.html");

function replaceOnce(pattern, replacement, label) {
  if (!pattern.test(html)) throw new Error(`Could not locate the ${label} block in index.html`);
  html = html.replace(pattern, replacement);
}

replaceOnce(/  <style>[\s\S]*?  <\/style>/, `  <style>\n${read("styles.css")}\n  </style>`, "style");
replaceOnce(/  <script>\s*\/\/ Generated by scripts\/build-question-banks\.js[\s\S]*?  <\/script>/, `  <script>\n${read("question-banks.js")}\n  </script>`, "question bank");
replaceOnce(/  <script>\s*"use strict";\s*\(\(\) => \{\s*const GROUPS[\s\S]*?  <\/script>/, `  <script>\n${read("curriculum.js")}\n  </script>`, "curriculum");
replaceOnce(/  <script>\s*"use strict";\s*const curriculum = window\.COOL_CALL_CURRICULUM;[\s\S]*?  <\/script>/, `  <script>\n${read("app.js")}\n  </script>`, "application");

fs.writeFileSync(path.join(root, "index.html"), `${html}\n`, "utf8");
console.log("Rebuilt standalone index.html");
